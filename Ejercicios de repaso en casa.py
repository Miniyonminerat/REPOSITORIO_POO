class Perro:
    
    def __init__(self, nombre, edad):

        self.nombre = nombre

        self.edad = edad

    def ladrar(self):

        print(f"{self.nombre} el perro esta ladrando.")

    def cumplir_años(self):

        self.edad += 1

        print(f"{self.nombre} ahora tiene {self.edad} años.")
















